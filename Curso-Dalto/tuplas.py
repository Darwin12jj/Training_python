#Creación de tupla con TUPLE
#Las tuplas se crean cuando son datos únicamente de texto.
tupla = tuple(["dato1", "dato2"])


#Si se colocan los datos únicamente separados por la "," se gnerará una tupla sin necesidad del paréntesis.
tupla = "dato1", "dato2"

#creando una tupla sin paréntesis de un solo dato
tupla = "dato",
print (tupla)